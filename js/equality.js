let v1 = 1;
let v2 = "1";
console.log(v1==v2); // == (double equal to) checks whether VALUE is same or not
// Meaning == allows type conversion (coercion) to happen

console.log(v1===v2); // === (triple equal to) checks whether VALUE as well as TYPE is same or not
// Meaning === DOESN'T allow type conversion (coercion) to happen